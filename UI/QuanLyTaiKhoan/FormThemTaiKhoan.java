    private void buttonXacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonXacNhanActionPerformed
        //Làm gì đó thêm tài khoản
        TaiKhoanDTO newNhanVienDTO = new TaiKhoanDTO(
                null,
                this.textfeildTenNhanVien.getText(),
                this.textfeildSDT.getText(),
                this.comboxLoai.getSelectedIndex(),
                this.textfieldTenDangNhap.getText(),
                new String(this.passwordFeild.getPassword())
        );

        boolean themResult;
        try {
            themResult = TaiKhoanBUS.getInstance().themNhanVien(newNhanVienDTO);
            String messageResults = "Thêm tài khoản " + newNhanVienDTO.getHO_TEN() + " " + (themResult ? "thành công" : "thất bại");
            JOptionPane.showMessageDialog(this, messageResults, "Kết quả thêm tài khoản", JOptionPane.INFORMATION_MESSAGE);
        } catch (TaiKhoanValidateException ex) {
            String messageResults = "Thêm tài khoản " + newNhanVienDTO.getHO_TEN() + " " + "thất bại do " + ex.getMessage();
            JOptionPane.showMessageDialog(this, messageResults, "Kết quả thêm tài khoản", JOptionPane.INFORMATION_MESSAGE);
        }

        //Sau đó làm phần AfterThem
        if (this.afterThemNhanVien != null) {
            this.afterThemNhanVien.DoAfterThemTaiKhoan();
        }
    }//GEN-LAST:event_buttonXacNhanActionPerformed

